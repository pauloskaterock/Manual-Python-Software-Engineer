# Em Python, os operadores aritméticos são usados para realizar operações matemáticas em valores numéricos. Aqui estão os operadores aritméticos básicos com exemplos de código:

# 1. Adição (+)
# Soma dois valores.


a = 5
b = 3
resultado = a + b
print(resultado)  # Saída: 8


# 2. Subtração (-)
# Subtrai o segundo valor do primeiro.


a = 5
b = 3
resultado = a - b
print(resultado)  # Saída: 2


# 3. Multiplicação (*)
# Multiplica dois valores.


a = 5
b = 3
resultado = a * b
print(resultado)  # Saída: 15


# 4. Divisão (/)
# Divide o primeiro valor pelo segundo. O resultado é um número de ponto flutuante.


a = 5
b = 3
resultado = a / b
print(resultado)  # Saída: 1.6666666666666667


# 5. Divisão inteira (//)
# Divide o primeiro valor pelo segundo e retorna a parte inteira do quociente.


a = 5
b = 3
resultado = a // b
print(resultado)  # Saída: 1

# 6. Módulo (%)
# Retorna o resto da divisão do primeiro valor pelo segundo.


a = 5
b = 3
resultado = a % b
print(resultado)  # Saída: 2



# 7. Exponenciação (**)

# Eleva o primeiro valor à potência do segundo.

a = 5
b = 3
resultado = a ** b
print(resultado)  # Saída: 125


# 8. Negação (-)
# Inverte o sinal de um valor.


a = 5
resultado = -a
print(resultado)  # Saída: -5
# Exemplos adicionais:
# Incremento e Decremento:
# Embora Python não tenha operadores de incremento (++) e decremento (--) como outras linguagens, é possível incrementar e decrementar valores usando os operadores de adição e subtração:

a = 5
a += 1  # Incrementa a em 1
print(a)  # Saída: 6

a -= 1  # Decrementa a em 1
print(a)  # Saída: 5
# Operações combinadas:
# Os operadores aritméticos podem ser combinados para formar expressões mais complexas.

a = 5
b = 3
c = 2
resultado = (a + b) * c - (a / b)
print(resultado)  # Saída: 15.333333333333334
# Resumo:
# +: Adição
# -: Subtração
# *: Multiplicação
# /: Divisão
# //: Divisão inteira
# %: Módulo
# **: Exponenciação
# -: Negação (inversão de sinal)
# +=: Incremento
# -=: Decremento


# Esses operadores são fundamentais em Python para realizar operações matemáticas e são amplamente utilizados em diversos contextos de programação.







