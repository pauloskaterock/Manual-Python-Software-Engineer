# Biblioteca BeautifulSoup
# BeautifulSoup é uma biblioteca Python usada para extrair dados de arquivos HTML e XML. Ela cria uma árvore de análise a partir de documentos XML ou HTML, que pode ser usada para extrair dados de maneira eficiente. BeautifulSoup é frequentemente usada em conjunto com a biblioteca requests para realizar web scraping.

# Instalação

# pip install beautifulsoup4
# pip install requests

